        <div class="sized-box"></div>
        <p>What is anxiety?</p>
        <p>Anxiety is your body’s natural response to stress. It’s a feeling of fear or worry that could be caused by a combination of factors that researchers believe range from genetics to environmental to brain chemistry.</p>
      </div>
      <div class="card">
        <h2>Some common symptoms of anxiety include:</h2>
        <p>Increased heart rate</p>
        <p>rapid breathing</p>
        <p>restlessness</p>
        <p>trouble concentrating</p>
      </div>
      <div class="card">
        <h2>Manifestations of anxiety</h2>
        <p>However, it’s important to note that anxiety can present itself in different ways for different people. While one person may experience a butterfly feeling in their stomach, another might have panic attacks, nightmares, or painful thoughts.</p>
      </div>
      <div class="card">
        <p>With that being said, there’s a difference between everyday anxiety and anxiety disorders. Feeling anxious about something new or stressful is one thing, but when it gets to an uncontrollable or excessive point and starts to affect your quality of life, it could be a disorder.</p>
      </div>
      <div class="card">
        <h2>Some anxiety disorders include:</h2>
        <p>Panic Disorder</p>
        <p>Post-Traumatic Stress Disorder</p>
        <p>Obsessive-Compulsive Disorder</p>
        <p>Separation Anxiety</p>
        <p>Phobias</p>
      </div>
      <div class="card">
        <h2>How can I treat anxiety?</h2>
        <p>Anxiety can be treated in a variety of ways. One common treatment option is cognitive behavioral therapy (CBT), which helps provide people with tools to cope with anxiety when it occurs.</p>
        <p>There are also certain medications, like antidepressants and sedatives, that work to balance brain chemistry and prevent episodes of anxiety. They may even ward off the most severe symptoms.</p>
      </div>
      <div class="card">
        <h2>Natural Method</h2>
        <p>You can make adjustments to habits, like exercise, sleep, and diet. You can also try something totally new, like aromatherapy or meditation. No matter what your lifestyle demands, there’s a natural way to help reduce anxiety for everyone.</p>
      </div>
      <div class="card">
        <h2>10 Remedies for anxiety</h2>
      </div>
      <div class="card">
        <h2>1. Stay active</h2>
        <p>Regular exercise isn’t just about physical health — it can be a huge help to your mental health, as well.</p>
      </div>
      <div class="card">
        <h2>2. Steer clear of alcohol</h2>
        <p>Drinking alcohol may take the edge off at first, since it’s a natural sedative. However, research suggests there’s a link between anxiety and alcohol consumption, with anxiety disorders and alcohol use disorder (AUD) occurring hand-in-hand.</p>
        <p>Alcohol has also been shown to disrupt your body’s natural ability to sleep by interfering with sleep homeostasis. And as we’ll later point out, a good night’s sleep is incredibly helpful when combating anxiety.</p>
      </div>
      <div class="card">
        <h2>3. Consider quitting smoking cigarettes</h2>
        <p>Smokers often reach for a cigarette during stressful times. Yet, like drinking alcohol, taking a drag on a cigarette when you’re stressed is a quick fix that may worsen anxiety over time.</p>
        <p>Research has shown that the earlier you start smoking in life, the higher your risk of developing an anxiety disorder later. Research also suggests nicotine and other chemicals in cigarette smoke alter pathways in the brain linked to anxiety.</p>
      </div>
      <div class="card">
        <h2>4. Limit caffeine intake</h2>
        <p>If you have chronic anxiety, caffeine is not your friend. Caffeine may cause nervousness and jitters, neither of which is good if you’re anxious. Caffeine may cause or worsen anxiety disorders. It may also cause panic attacks in people with panic disorder. In some people, eliminating caffeine may significantly improve anxiety symptoms.</p>
      </div>
      <div class="card">
        <h2>5. Prioritize getting a good night’s rest</h2>
        <p>Sleep has been proven time and time again to be an important part of good mental health.</p>
      </div>
      <div class="card">
        <h2>6. Meditate and practice mindfulness</h2>
        <p>A main goal of meditation is full awareness of the present moment, which includes noticing all thoughts in a nonjudgmental way. This can lead to a sense of calm and contentment by increasing your ability to mindfully tolerate all thoughts and feelings.</p>
      </div>
      <div class="card">
        <h2>7. Eat a balanced diet</h2>
        <p>Low blood sugar levels, dehydration, or chemicals in processed foods, such as artificial flavorings, artificial coloring, and preservatives, may cause mood changes in some people. A high-sugar diet may also impact temperament.</p>
      </div>
      <div class="card">
        <h2>8. Practice deep breathing</h2>
        <p>Shallow, fast breathing is common with anxiety. It may lead to a fast heart rate, dizziness or lightheadedness, or even a panic attack. Deep breathing exercises — the deliberate process of taking slow, even, deep breaths — can help restore normal breathing patterns and reduce anxiety.</p>
      </div>
      <div class="card">
        <h2>9. Try alternative methods such as aromatherapy</h2>
        <p>Aromatherapy is a holistic healing treatment that has been used by humans for thousands of years. The practice uses natural plant extracts and essential oils to promote the health and well-being of the mind, body, and spirit. Its goal is to enhance both physical and emotional health.</p>
      </div>
      <div class="card">
        <h2>10. Drink chamomile tea</h2>
        <p>A cup of chamomile tea is a common home remedy to calm frayed nerves and promote sleep.</p>
